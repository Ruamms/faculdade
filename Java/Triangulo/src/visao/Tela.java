    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
    Triangulo t = new Triangulo();
        
    try {
        t.valor1 = Double.parseDouble(txt1.getText());
        t.valor2 = Double.parseDouble(txt2.getText());
        t.valor3 = Double.parseDouble(txt3.getText());
            
        txtResultado.setText(t.equil());
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Informe valores validos");
    }
    }//GEN-LAST:event_btncalcularActionPerformed

