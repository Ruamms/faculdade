    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        DecimalFormat df = new DecimalFormat("0.00");
        IMC i = new IMC();
        try {
            String nome = txtNome.getText();
            i.peso = Double.parseDouble(txtpeso.getText());
            i.altura = Double.parseDouble(txtaltura.getText());

            txtresultado.setText("Nome: " + nome + "\nIMC: " + df.format(i.calcIMC()) + i.resultadoIMC());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "informe o valor v√°lido");
        }
    }//GEN-LAST:event_btncalcularActionPerformed

